package Behavioral.BaiTap4;

/**
 *
 * @author HP
 */
public class DuAn {
    String tenDA;
    int soTienVay;

    public DuAn(String tenDA, int soTienVay) {
        this.tenDA = tenDA;
        this.soTienVay = soTienVay;
    }

    public String getTenDA() {
        return tenDA;
    }

    public void setTenDA(String tenDA) {
        this.tenDA = tenDA;
    }

    public int getSoTienVay() {
        return soTienVay;
    }

    public void setSoTienVay(int soTienVay) {
        this.soTienVay = soTienVay;
    }
    
    
}
